package com.netcabs.datamodel;

import java.util.ArrayList;

public class FastTripsInfo {
	private String id;
	private UserInfo user;
	private String name;
	private String pickupName;
	private double pickupLat;
	private double pickupLong;
	private String destinationName;
	private double destinationLat;
	private double destinationLong;
	private String date;
	private String fare;
	private String distance;
	private String paymentType;
	
	private ArrayList<JourneyReportInfo> journeyReportInfo;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPickupName() {
		return pickupName;
	}

	public void setPickupName(String pickupName) {
		this.pickupName = pickupName;
	}

	public double getPickupLat() {
		return pickupLat;
	}

	public void setPickupLat(double pickupLat) {
		this.pickupLat = pickupLat;
	}

	public double getPickupLong() {
		return pickupLong;
	}

	public void setPickupLong(double pickupLong) {
		this.pickupLong = pickupLong;
	}

	public String getDestinationName() {
		return destinationName;
	}

	public void setDestinationName(String destinationName) {
		this.destinationName = destinationName;
	}

	public double getDestinationLat() {
		return destinationLat;
	}

	public void setDestinationLat(double destinationLat) {
		this.destinationLat = destinationLat;
	}

	public double getDestinationLong() {
		return destinationLong;
	}

	public void setDestinationLong(double destinationLong) {
		this.destinationLong = destinationLong;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getFare() {
		return fare;
	}

	public void setFare(String fare) {
		this.fare = fare;
	}

	public String getDistance() {
		return distance;
	}

	public void setDistance(String distance) {
		this.distance = distance;
	}

	public String getPaymentType() {
		return paymentType;
	}

	public void setPaymentType(String paymentType) {
		this.paymentType = paymentType;
	}

	public UserInfo getUser() {
		return user;
	}

	public void setUser(UserInfo user) {
		this.user = user;
	}

	public ArrayList<JourneyReportInfo> getJourneyReportInfo() {
		return journeyReportInfo;
	}

	public void setJourneyReportInfo(ArrayList<JourneyReportInfo> journeyReportInfo) {
		this.journeyReportInfo = journeyReportInfo;
	}

}
